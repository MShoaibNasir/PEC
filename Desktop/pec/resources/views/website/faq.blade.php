<li>In case of a project dispute, PEC will play the role of third party arbitrator for resolution of conflict against a fee. Both parties in the agreement will bear the fees pertaining to dispute adjudication collectively.</li>
<li>Arbitrator:</li>
<li>In case of disputes between client and consultant; either party can report a dispute at PEC Gateway for conduct of arbitration as 3<sup>rd</sup> party mediator. Arbitrator will collect necessary information from client and consultant to commence activities of dispute adjudication.</li>
